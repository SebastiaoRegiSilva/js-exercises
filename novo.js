// Laço aninhado.

// Para cada.
for (var adj of [
    'Raging',
    'Happy',
    'Hungry'
]) {
    for (var noun of [
        'Pharmacists',
        'Squids',
        'Twins'
    ]) {
        print('The ' + adj + ' ' + noun);
    }
}

// Os laços aninhados farão todas as combinações das 2 arrays.